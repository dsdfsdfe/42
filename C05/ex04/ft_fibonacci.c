int		ft_fibonacci(int index)
{
	int i;

	i = index;
	if (i < 0)
		return (-1);
	else if (i == 0 || i == 1)
		return (i);
	return (ft_fibonacci(i - 1) + ft_fibonacci(i - 2));
}
